package paket;

public class AritmetickeOperacije {

	// Metoda za sabiranje dva cijela broja

	static int saberi(int x, int y) {
		return x + y;
	}

	// Metoda za oduzimanje dva cijela broja
	static int oduzmi(int x, int y) {
		return x - y;
	}

	// Metoda mnozenja dva cijela broja
	static int mnozenje(int x, int y) {
		return x * y;
	}

	// Metoda mnozenja dva realna broja
	static double mnozenje(double x, double y) {
		return x * y;

	}

	// Metoda za deljenje dva realna broja
	static double podeli(double x, double y) {
		return x / y;
	}

	// Metoda za deljenje dva cijela broja
	static int podeliCelobrojno(int x, int y) {
		return x / y;
	}

	// Metoda za nalazenje modula dva cijela broja
	static int nadjiModul(int x, int y) {
		return x % y;
	}

}
